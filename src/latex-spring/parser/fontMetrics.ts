type FONT_METRICS_TYPE = typeof FONT_METRICS
export type FONT_FAMILIES = keyof FONT_METRICS_TYPE
export type FONT_STYLES = 'normal' | 'italic' | 'bold'

type GetFontMetricsArgs = {
    char: string
    fontSize: number
    fontStyle: FONT_STYLES
    fontFamily: FONT_FAMILIES
}
type GetStringMetricsArgs = Omit<GetFontMetricsArgs, 'char'> & { str: string }

export const getFontMetrics: (
    args: GetFontMetricsArgs
) => { width: number; ascent: number; descent: number } = ({
    char,
    fontFamily,
    fontSize,
    fontStyle,
}) => {
    let charMetrics: number[]

    try {
        charMetrics = FONT_METRICS[fontFamily][char][fontStyle]
    } catch (er) {
        throw new Error(
            `no font metrics could be found for char: ${char} with fontFamily:${fontFamily} , fontStyle:${fontStyle}, fontStyle:${fontStyle}`
        )
    }
    return {
        width: fontSize * charMetrics[0],
        ascent: fontSize * charMetrics[1],
        descent: fontSize * charMetrics[2],
    }
}
export const getStringMetrics: (
    args: GetStringMetricsArgs
) => { width: number; maxAscent: number; maxDescent: number } = ({
    str,
    fontFamily,
    fontStyle,
    fontSize,
}) => {
    // strWidth, maxAscent, maxDescent
    let strWidth = 0,
        maxAscent = 0,
        maxDescent = 0
    const chars = str.split('')

    for (const char of chars) {
        const { width, ascent, descent } = getFontMetrics({
            char,
            fontFamily,
            fontSize,
            fontStyle,
        })
        strWidth += width
        if (ascent > maxAscent) {
            maxAscent = ascent
        }
        if (descent > maxDescent) {
            maxDescent = descent
        }
    }

    return { width: strWidth, maxAscent, maxDescent }
}

